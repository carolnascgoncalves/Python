a = 10.2;
b = "Oi";

# Float e String
print(type(a));
print(type(b));

# Transforma ambos pra booleano (True/False)
a = bool(a);
b = bool(b);

# Para tudo que é diferente de zero, é sempre True e 1
print("Bool pra int: ")
print(int(a));
print(int(b));

# Concatenação: 
print(f"A varíavel B é: {b} e a A é: {int(a)}");